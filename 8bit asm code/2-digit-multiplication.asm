;2-digit-Multiplication
DATA SEGMENT
TEMP DB 10 DUP(?)
NUM1 DB ?
NUM2 DB ?
RESULT DB ? 
PROD    DB  ? 
MSG1 DB 10,13,"ENTER FIRST NUMBER TO MULTIPLY : $"
MSG2 DB 10,13,"ENTER SECOND NUMBER TO MULTIPLY : $"
MSG3 DB 10,13,"RESULT OF MULTIPLICATION IS : $"
ENDS

CODE SEGMENT
ASSUME DS:DATA CS:CODE 

START:
MOV AX,DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,9
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
MOV CH,AL
MOV AH,1
INT 21H


SUB AL,30H 
MOV CL,AL
MOV AL, 10000B 
MUL CH 
XOR AH, AH
OR AL, CL   
MOV NUM1, AL
MOV AL, 0H

LEA DX,MSG2
MOV AH,9
INT 21H 

MOV AH,1
INT 21H
SUB AL,30H
MOV CH,AL
MOV AH,1
INT 21H

SUB AL,30H
MOV CL,AL
MOV AL,10000B
MUL CH
XOR AH,AH
OR  AL,CL
MOV NUM2,AL
MOV AL,0H

MOV AL,0H
MOV DH,0H
MOV AL,NUM1
MOV DL,NUM2
MUL DL



MOV PROD,AL

MOV CX,00H
MOV BL,10000B
XOR BH,BH
XOR SI,SI 

 

LOOP1:
MOV DX,00
DIV BX          ; BH+BL
MOV TEMP[SI],DL
INC SI 
INC CX 
OR AX,AX
JNZ LOOP1
DEC SI 

MOV AL,PROD 


LEA DX,MSG3
MOV AH,09
INT 21H

LOOP2:
MOV DL, TEMP[SI] 
MOV AH,06H
ADD DL,30H
INT 21H 
DEC SI
DEC CX
JNZ LOOP2  



ENDS
END START